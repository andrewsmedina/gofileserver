package fcrypt

import (
	"crypto/md5"
	"fmt"
)

func Md5(text string) string {

	hash := md5.New()
	b := []byte(text)
	hash.Sum(b)
	hash.Write(b)

	return fmt.Sprintf("%x", hash.Sum(nil))

}
